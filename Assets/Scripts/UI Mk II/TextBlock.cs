using UnityEngine;
using System.Collections;
using System.Collections.Generic;
using System.Linq;

[RequireComponent(typeof(MeshFilter))]
[RequireComponent(typeof(MeshRenderer))]
[ExecuteInEditMode]
public class TextBlock : MonoBehaviour 
{
    [TextArea(3,10)]
    public string Text;

    string meshText = "";

    public float LetterSpacing = 0.1f;
    public float LineHeight = 0.95f;

    MeshFilter meshFilter;

	void Start () 
    {
        updateMesh();
	}

    void updateMesh()
    {
        if (meshFilter == null) meshFilter = gameObject.GetComponent<MeshFilter>();

        var mesh = new Mesh();
        var vertices = new List<Vector3>();
        var triangles = new List<int>();
        var verticalOffset = Vector3.zero;

        var lines = Text.Split('\n');
        foreach(var line in lines)
        {
            addLine(line, verticalOffset, vertices, triangles);
            verticalOffset += new Vector3(0, -LineHeight, 0);
        }
        
        mesh.vertices = vertices.ToArray();
        mesh.triangles = triangles.ToArray();
        mesh.normals = Enumerable.Repeat(new Vector3(0, 0, -1), vertices.Count).ToArray();
        mesh.RecalculateBounds();
        meshFilter.sharedMesh = mesh;
    }

    public void addLine(string line, Vector3 offset, List<Vector3> vertices, List<int> triangles)
    {
        line = line.Trim();

        var vertOffset = Vector3.zero;
        foreach (char c in line)
        {
            if (CharacterData.characters.ContainsKey(c))
            {
                var characterData = CharacterData.characters[c];

                int indexOffset = vertices.Count;
                foreach (var vertex in characterData.Vertices)
                {
                    vertices.Add(vertex + vertOffset + offset);
                }

                foreach (var index in characterData.Polygons)
                {
                    triangles.Add(index + indexOffset);
                }

                vertOffset += new Vector3(characterData.Size.x + LetterSpacing, 0, 0);
            }
            else vertOffset += new Vector3(0.5f, 0, 0);
        }
    }
	
	void Update () 
    {
        if (Text != meshText) updateMesh();
	}
}


